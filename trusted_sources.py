def trusted_site_query(limit: int = 80) -> str:
    domains = TRUSTED_SOURCE_DOMAINS[:limit]
    return " OR ".join(f"site:{domain}" for domain in domains)


def trusted_source_prompt(limit: int = 120) -> str:
    domains = TRUSTED_SOURCE_DOMAINS[:limit]
    return "\n".join(f"- {domain}" for domain in domains)
